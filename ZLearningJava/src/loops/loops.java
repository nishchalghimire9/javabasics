package loops;

public class loops {

	public static void main(String[] args) {
		// While loop
		int d = 0;
		while(d<=100) {
			System.out.println("condition is true: "+  d);
			d++;
		}
		
		int s = 100; // writing number in descending order.
		while(s>=0) {
			System.out.println("condition is true: "+  s);
			s--;
		}
		

	}

}
